<a name="barc">
Circulação com ou sem etiquetas de barra de código</a>:
<br>
Os formulários de circulação do OpenBilio são designados a suportar uma eficiente circulação atravéz do uso de um scanner para código de barras e os cartões dos membros e etiquetas nos exemplares com o <a href="../shared/help.php?page=barcodes">código de barras</a>.
<br>
É possível circular quando não se conhece o código de barras, procurando membros pelo sobrenome e pelo OPAC atravéz da <a href="../shared/help.php?page=opacLookup">Visualização do Código de Barras</a>, reconhecendo assim o código do exemplar para retirada, devolução ou reserva.
<br><br><br>

<a name="late">
Atrasos não notificados</a>:
<br>
O OpenBiblio não notifica os voluntários quando estiver checando atrasos e calculando multas.
Dependendo das regras da biblioteca e do método de operação do OpenBiblio pode levar a um cálculo de multas injusto aos membros. Para evitar isto, vá à página de Gêneros (na aba Administração) e coloque Multas Diárias para todos os gêneros como zero.