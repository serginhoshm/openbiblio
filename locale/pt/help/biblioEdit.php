<h1>Nova Bibliografia e Editar Basic:</h1>
As páginas de Nova Bibliografia e Editar Bibliografia (Editar Basic) são baseados no formato <a href="javascript:popSecondaryLarge('http://www.loc.gov/marc')">MARC 21</a>. Somente uma pequena parcela da enorme quantidade de campos do MARC estão inclusas nas páginas para auxiliar no adicionamento de bibliografias. Uma vez adicionado a bibliografia, você pode entaum adicionar qualquer um dos campos do MARC21 utilizando a página do Editar MARC.
<br><br>
Uma vez adicionado uma nova bibliografia, você precisará adicionar novos exemplares para esta bibliografia antes de liberá-lo para emprestimo.
