<h1>Entendendo a mudança de Status de uma bibliografia:</h1>
A tabela a seguir lista os possiveis status para um exemplar de uma bibliografia.<br><br>
<table class="primary">
  <tr><th>Status</th><th>Descrição</th></tr>
  <tr><td class="primary" valign="top">devolvido</td><td class="primary">A bibliografia está na prateleira e pronto para empréstimo.</td></tr>
  <tr><td class="primary" valign="top">emprestado</td><td class="primary">A bibliografia foi emprestado a um membro da biblioteca.</td></tr>
  <tr><td class="primary" valign="top">reservado</td><td class="primary">A bibliografia está reservado a um membro da biblioteca para empréstimo.</td></tr>
  <tr><td class="primary" valign="top">em espera</td><td class="primary">Bibliografia na prateleira à espera para ser retirado.</td></tr>
  <tr><td class="primary" valign="top">danificado/conserto</td><td class="primary">A bibliografia está danificada e precisa ser reparada.</td></tr>
  <tr><td class="primary" valign="top">área de exposição</td><td class="primary">A bibliografia não esta avaliada por estar na are de avaliação.</td></tr>
  <tr><td class="primary" valign="top">perdido</td><td class="primary">A bibliografia não esta avaliada porque não pôde ser encontrada.</td></tr>
  <tr><td class="primary" valign="top">sob empréstimo</td><td class="primary">A bibliografia está sob empreéstimo.</td></tr>
  <tr><td class="primary" valign="top">em ordem</td><td class="primary">A biliografia foi lançada, porém ainda não avaliada na biblioteca.</td></tr>
</table>
